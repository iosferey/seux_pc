def _build_country_lookup():
    lookup = {}

    for code, profile in HOFSTEDE.items():
        lookup[_normalize_key(code)] = code
        lookup[_normalize_key(profile.get("country"))] = code

    for alias, code in COUNTRY_ALIASES.items():
        if code in HOFSTEDE:
            lookup[_normalize_key(alias)] = code

    return lookup


COUNTRY_LOOKUP = _build_country_lookup()


def normalize_country_code(value):
    key = _normalize_key(value)
    return COUNTRY_LOOKUP.get(key)


def get_country_profile(value):
    code = normalize_country_code(value)

    if not code:
        return None

    return HOFSTEDE.get(code)


def get_dimension_value(profile, dimension, default=0):
    value = profile.get(dimension) if isinstance(profile, dict) else None

    if value is None:
        return default

    return value


def comparable_dimensions(source, target):
    return [
        dimension
        for dimension in CULTURAL_DIMENSIONS
        if get_dimension_value(source, dimension, None) is not None
        and get_dimension_value(target, dimension, None) is not None
    ]
